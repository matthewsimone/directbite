// ============================================================================
// gen-domain-map.mjs — build-time custom_domain → slug map generator
// ============================================================================
//
// Emits src/lib/domainMap.js: an Edge-safe ES module (plain object literal)
// mapping a normalized custom-domain host → restaurant slug. Consumed by the
// Edge middleware to rewrite custom-domain requests to the prerendered
// dist/<slug>/… pages WITHOUT a per-request DB lookup.
//
// Rationale: a newly-attached custom domain can't serve prerendered pages
// until a deploy regenerates them anyway, so the map is exactly as fresh as
// the prerendered output — a runtime lookup would add permanent latency and a
// failure mode to solve a problem that can't occur.
//
// Runs FIRST in vercel-build (before vite build + prerender) so the generated
// module exists when Vite/Vercel bundle the middleware. Uses the same build
// client bootstrap as scripts/prerender-test-home.mjs (createServer +
// ssrLoadModule → getBuildClient); it does NOT need dist/, only the DB.
//
// The OUTPUT file is a bare object literal — no Node built-ins — so it imports
// cleanly into the Edge Runtime. This generator script itself uses Node freely.
// ============================================================================

import { createServer } from 'vite'
import { promises as fs } from 'node:fs'
import path from 'node:path'

const OUT_PATH = path.resolve('src/lib/domainMap.js')

// Normalize a host the SAME way the middleware does (middleware.js:10) so the
// generated keys match the incoming normalized host at lookup time.
const normalizeHost = (h) => (h || '').toLowerCase().replace(/^www\./, '').split(':')[0]

async function main() {
  const vite = await createServer({
    server: { middlewareMode: true },
    appType: 'custom',
    logLevel: 'warn',
  })

  try {
    const { getBuildClient } = await vite.ssrLoadModule('/src/lib/supabaseBuild.js')
    const supabase = getBuildClient()

    const { data: rows, error } = await supabase
      .from('restaurants')
      .select('custom_domain, slug')
      .eq('website_enabled', true)
      .not('custom_domain', 'is', null)
    if (error) throw error

    // Build normalized host → slug. Warn (last-write-wins) on any collision,
    // e.g. two rows whose custom_domain normalizes to the same host.
    const map = {}
    for (const r of rows || []) {
      const host = normalizeHost(r.custom_domain)
      if (!host || !r.slug) continue
      if (map[host] && map[host] !== r.slug) {
        console.warn(`⚠ domain collision: '${host}' maps to both '${map[host]}' and '${r.slug}' — keeping '${r.slug}'`)
      }
      map[host] = r.slug
    }

    // Deterministic output: sort keys so the generated file only changes when
    // the domain set actually changes (clean diffs).
    const keys = Object.keys(map).sort()
    const body = keys.map((host) => `  ${JSON.stringify(host)}: ${JSON.stringify(map[host])},`).join('\n')
    const content =
      '// AUTO-GENERATED by scripts/gen-domain-map.mjs — do not edit by hand.\n' +
      'export default {\n' +
      (body ? `${body}\n` : '') +
      '}\n'

    await fs.writeFile(OUT_PATH, content, 'utf-8')
    console.log(`✓ wrote ${path.relative(process.cwd(), OUT_PATH)} — ${keys.length} custom domains`)
  } finally {
    await vite.close()
  }
}

main().catch((err) => {
  console.error('gen-domain-map failed:', err)
  process.exit(1)
})
